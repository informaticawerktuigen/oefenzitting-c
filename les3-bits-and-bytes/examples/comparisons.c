#include <stdio.h>

int main(void)
{
    int i = 0;
    int j = 1;
    printf("equal: %d\n", i == j);
    printf("not equal: %d\n", i != j);
    printf("greater: %d\n", i > j);
    printf("less: %d\n", i < j);
    printf("less or equal: %d\n", i <= j);
    printf("bigger or equal: %d\n", i >= j);
    return 0;
}
